function add (a, b) {
  // console.log('add function');
  return a + b;
}
function subtract (a, b) {
  // console.log('subtract function');
  return a - b;
}
function multiply (a, b) {
  // console.log('multiply function');
  return a * b;
}
function divide (a, b) {
  // console.log('divide function');
  return a / b;
}

export {
  add,
  subtract,
  multiply,
  divide
};